<h3 class="text-center">6 Ways to Generate Multi-Family Apartment Investing Deals</h3>
<p>Lead generation is a foundational activity of your multi family investment business. Without it, you’ll fail before you even begin. Many people don’t think they have the time or the energy to find deals. I don’t care how busy people tell me they are. My response is always the same – if you want it bad enough, you’ll make time for the important things. When you discover how to buy apartments and how easy it is to get started, you’ll be kicking yourself for not starting earlier. Here are six ways to generate apartment investing deals.</p>

<p><strong>1) Direct Mail</strong></p>

<p>Direct mail is one of my favorites. In fact, it’s how I got started. After I would get home late at night, I’d always make sure I’d stuff envelopes and get those letters out. Why? Those letters represented my future success. There are many investors shunning the idea of snail mail, but the truth is, it’s still works and it’s still a great way to generate leads.</p>

<p><strong>2) Reading the Classifieds</strong></p>

<p>With the number of short sales, pre-foreclosures, and foreclosures on the market, you’ll be sure to find something in the classifieds section of your local newspaper. Be diligent and read each ad. The more ads you read, the more you’ll become familiar with them and know how to spot a potential deal. That’s what I did and after awhile, I started calling these people. The more ads I called, the easier it got to make those calls and eventually, I ended up getting a couple of deals that way.</p>
