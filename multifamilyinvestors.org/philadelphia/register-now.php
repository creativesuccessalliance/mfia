        <h3 class="text-center">Why Apartment Investing is Good for Me?</h3>
        <div class="row">
            <div class="col-sm-12">

                <p>Now is a great time to purchase multi family homes! Let’s face it; we are now living in a buyers market. With more and more multi family homes on the market, sellers are facing some stiff competition, and are being forced to lower their prices in order to contend with other hungry sellers. This leaves ambitious buyers with a great opportunity, however, while timing could not be more perfect to learn how to buy properties, it is vital to know what kind of multi family investment is going to best maximize your profits. After all, the goal of apartment investing is making as much money as possible, while spending little.</p>
<img src="img/apartment-building-small.jpg" class="pull-right img-thumbnail" style="margin-left: 10px">
<p>When most people think about apartment investing, the first thing that comes to their mind is duplexes and single-family homes. This is because many people are under the false impression that certain money saving strategies like fix and flip, and no money down is only available when buying single-family units. In fact, these same options are available when purchasing multi-family homes like apartment buildings. In reality, purchasing an apartment building is often the smarter, and most cost effective choice. An apartment building can be easier to occupy, less time consuming, and can also be the most efficient way to maximize your profit.</p>
<p>With the demand for houses increasing in today’s buyers market, developers are continuing to build more homes. As we all know “demand” gives these developers the power to price these new properties as they see fit, and that price is more than likely going to be high, however, many people cannot afford to pay the $1,200.00 mortgage that comes along with the increased price of the home and look to a more affordable way to live. In this case, apartments, with their lower monthly rent, become more appealing to buyers.</p>
